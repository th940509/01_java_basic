/*
 * 
 * 변수 명명 규칙
 * 
 * 1) 절대적 규칙
 * 
 *  - 변수명에 숫자는 사용가능 하나 숫자로 시작할 수는 없다.
 *  - 변수명에 특수문자는 _, $만 가능하다. ($는 거의 사용하지 않음.)
 *  - 변수명에 예약어(키워드)는 사용할 수 없다. ex) class, int, public
 *  - 변수명에 띄어쓰기를 사용할 수 없다.
 * 
 * 2) 암묵적 규칙
 * 
 *  - '제 3자' 보아도 변수안에 어떤 데이터가 저장되어 있을 지 '직관적'으로 알 수 있도록 변수명을 구체적으로 작성한다. (가장 중요)
 *     EX) a , b , c ( 절대 안됨 )
 *        productCode ( o )
 *        productName ( o )
 *        
 *  - 변수명은 영어로 작성한다. (한글X)
 *  - 변수명은 소문자로 시작한다. (클래스 이름을 대문자로 시작한다.)
 *  - 불리언타입 변수명 앞에는 is를 붙인다.
 *    Ex) isRunning, isChecked
 *  - 두 단어가 이어지는 경우 이어지는 단어의 첫글자를 대문자로 작성한다.(Camel Case) - 웹파트는 주로 카멜 케이스 사용   
 *    Ex) studentName, productName, productCode
 *  - 두 단어가 이어지는 경우 이어지는 단어를 _로 이어준다. (snake_case)
 *    Ex) product_name, product_code
 *    
 *  - 상수명은 전부 대문자로 작성한다.
 *  - 상수명에서 두 단어가 이어지는 경우 _로 이어준다. (snake_case)
 * */
